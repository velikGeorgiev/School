public class _17_PrinNumberFrom100To0 {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        int start = 100; // Inicio
        int end = 0; // Final
        
        for(; start >= end; start--) {
            System.out.println(start);
        }
    }

}
