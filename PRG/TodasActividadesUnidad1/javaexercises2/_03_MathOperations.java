/**
 *
 * @author velko
 */
public class _03_MathOperations {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        int numOne = 20;
        int numTwo = 2;
        
        System.out.printf("%d - %d = %d\n", numOne, numTwo, (numOne - numTwo));
        System.out.printf("%d + %d = %d\n", numOne, numTwo, (numOne + numTwo));
        System.out.printf("%d * %d = %d\n", numOne, numTwo, (numOne * numTwo));
        System.out.printf("%d / %d = %d\n", numOne, numTwo, (numOne / numTwo));
        System.out.printf("%d %% %d = %d\n", numOne, numTwo, (numOne % numTwo));
    }

}
